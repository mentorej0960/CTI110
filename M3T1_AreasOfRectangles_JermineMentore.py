lengthR1= float( input("Please enter length of rectangle 1: "))
widthR1 = float( input("Please enter width of rectangle 1: "))
lengthR2= float( input("Please enter length of rectangle 2: "))
widthR2 = float( input("Please enter width of rectangle 2: "))
areaR1 = lengthR1 * widthR1
areaR2 = lengthR2 * widthR2
if areaR1 == areaR2:
    print("Rectangle 1 is equal to Rectangle 2")
elif areaR1 > areaR2:
    print("Rectangle 1 is larger than Rectangle 2")
else:
     print("Rectangle 2 is larger than rectangle 1")







